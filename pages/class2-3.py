for i in range(10):  # i是迴圈變數,range(10)是從0開始到9的數字
    print(i)  # 每次只從range(10)內印出一個數字

for i in range(2, 100):  # i是迴圈變數,range(2,100)是從2開始到9的數字
    print(i)  # 每次只從range(10)內印出一個數字

for i in range(
    2, 10, 2
):  # i是迴圈變數,range(10)是從2開始到99的數字,每次取出的樹數字偶數
    print(i)  # 每次只從range(2,100,2)內印出一個數字

for i in range(100, 3, -2):  # range(100,3,-2)是從100開始到3的數字,每次取出的樹數字奇數
    print(i)  # 每次只從range(100,3,-2)內印出一個數字,從100到4的偶數
